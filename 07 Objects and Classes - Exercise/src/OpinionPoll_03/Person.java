package OpinionPoll_03;

public class Person {
    //1. Fields
    private String name;
    private int age;

    //2. Constructor
    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    //. Methods
    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }
}
